    # Variant selectors. Pass one as the ``mode`` keyword to dispatch to
    # the matching worker binary at spawn time. The corresponding wheel
    # (amuse-nbody7, amuse-nbody7-sse, amuse-nbody7-avx, amuse-nbody7-gpu)
    # must be installed; an unknown mode falls back to the CPU worker.
    MODE_CPU = 'cpu'
    MODE_SSE = 'sse'
    MODE_AVX = 'avx'
    MODE_GPU = 'gpu'

    def __init__(self, mode=MODE_CPU, **keyword_arguments):
        CodeInterface.__init__(
            self,
            name_of_the_worker=self.name_of_the_worker(mode),
            **keyword_arguments,
        )

    def name_of_the_worker(self, mode):
        if mode == self.MODE_CPU:
            return 'nbody7_worker'
        if mode == self.MODE_SSE:
            return 'nbody7_sse_worker'
        if mode == self.MODE_AVX:
            return 'nbody7_avx_worker'
        if mode == self.MODE_GPU:
            return 'nbody7_gpu_worker'
        # Unknown mode -> fall back to the CPU worker (always shipped
        # by the base amuse-nbody7 wheel).
        return 'nbody7_worker'


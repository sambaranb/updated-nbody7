"""
AMUSE Python bindings for NBODY7.

The MPI-IPC worker is generated by ``amusifier`` from this file and built
against the Fortran module ``Nbody7Interface`` (implemented in
``interface.f90``). One Fortran-glue worker is compiled, then linked four
times against four per-variant static libraries to produce four binaries:

* ``nbody7_worker``      -- portable scalar (default; cpu wheel)
* ``nbody7_sse_worker``  -- x86 SSE intrinsics  (sse wheel)
* ``nbody7_avx_worker``  -- x86 AVX intrinsics  (avx wheel)
* ``nbody7_gpu_worker``  -- CUDA                (gpu wheel)

The user picks one at runtime via the ``mode`` keyword:

    code = Nbody7(mode=Nbody7Interface.MODE_AVX)
    code = Nbody7(mode='gpu')

The matching wheel must be installed (``amuse-nbody7-avx``,
``amuse-nbody7-gpu``, ...). Each variant wheel ships only the worker
binary; the Python interface here is shared across all variants.

This module exposes two classes:

* :class:`Nbody7Interface` -- the low-level legacy-function specification
  that mirrors every Fortran subroutine. It inherits the standard
  :class:`GravitationalDynamicsInterface` and :class:`GravityFieldInterface`
  so the bulk of the AMUSE community-code contract (``new_particle``,
  ``get_state``, ``evolve_model``, ``get_gravity_at_point``, ...) is
  picked up automatically; only NBODY7-specific extras are declared here.

* :class:`Nbody7` -- the high-level user-facing wrapper, inheriting
  :class:`GravitationalDynamics` and :class:`GravityFieldCode`. It wires
  the NBODY7 parameters into the AMUSE parameter machinery so a user can
  write ``code.parameters.RBAR = 1.0 | units.parsec`` etc.

Modelled on :mod:`amuse_nbody6xx.interface` (Maxwell X. TSAI/CAI, 2014)
and :mod:`amuse_phigrape.interface` (PhiGRAPE; per-variant worker
dispatch).
"""


class Nbody7(GravitationalDynamics, GravityFieldCode):
    """High-level NBODY7 community code.

    Usage::

        from amuse.units import units, nbody_system
        from amuse.community.nbody7.interface import Nbody7

        converter = nbody_system.nbody_to_si(1.0e4 | units.MSun, 1.0 | units.parsec)
        code = Nbody7(converter)
        code.parameters.RBAR  = 1.0 | units.parsec
        code.parameters.ZMBAR = 0.7 | units.MSun
        code.parameters.ETAI  = 0.04
        code.parameters.ETAR  = 0.04
        code.particles.add_particles(my_cluster)
        code.evolve_model(10.0 | units.Myr)
    """

    def __init__(self, convert_nbody=None,
                 mode=Nbody7Interface.MODE_CPU, **kargs):
        GravitationalDynamics.__init__(
            self,
            Nbody7Interface(mode=mode, **kargs),
            convert_nbody,
            **kargs,
        )

    # -----------------------------------------------------------------
    # State machine
    # -----------------------------------------------------------------

    def define_state(self, handler):
        GravitationalDynamics.define_state(self, handler)
        GravityFieldCode.define_state(self, handler)

        # Allow editing particle state in RUN; transition to a CHANGED
        # state and require synchronize_model to step back to RUN.
        # Mirrors the nbody6xx behaviour.
        handler.add_method("EDIT", "set_state")
        handler.add_method("EDIT", "set_velocity")
        handler.add_method("EDIT", "set_mass")
        handler.add_method("EDIT", "set_position")
        handler.add_method("CHANGED", "before_get_parameter")

        handler.add_transition("RUN", "CHANGED", "set_state", False)
        handler.add_transition("RUN", "CHANGED", "set_velocity", False)
        handler.add_transition("RUN", "CHANGED", "set_mass", False)
        handler.add_transition("RUN", "CHANGED", "set_position", False)
        handler.add_transition("CHANGED", "RUN", "synchronize_model")

        handler.add_method("CHANGED", "get_state")
        handler.add_method("CHANGED", "get_mass")
        handler.add_method("CHANGED", "get_position")
        handler.add_method("CHANGED", "get_velocity")

    # -----------------------------------------------------------------
    # Particle set extensions
    # -----------------------------------------------------------------

    def define_particle_sets(self, handler):
        # Pull in the standard mass / position / velocity / radius
        # wiring from GravitationalDynamics, then add NBODY7-specific
        # per-particle attributes on top.
        GravitationalDynamics.define_particle_sets(self, handler)
        handler.add_setter("particles", "set_name", names=("name",))
        handler.add_getter("particles", "get_name", names=("name",))
        handler.add_setter("particles", "set_kstar", names=("kstar",))
        handler.add_getter("particles", "get_kstar", names=("kstar",))

    # -----------------------------------------------------------------
    # Code-level properties (read-only). Accessed as ``code.tstar`` and
    # ``code.vstar`` from user scripts.
    # -----------------------------------------------------------------

    def define_properties(self, handler):
        # Pull in the standard GD properties first (kinetic_energy,
        # potential_energy, total_energy, total_mass, total_radius,
        # center_of_mass_position / _velocity, ...). Without this call
        # those attributes vanish from ``code.*`` because AMUSE only
        # registers what each define_properties override declares.
        GravitationalDynamics.define_properties(self, handler)

        # NBODY7-specific physical-units conversion factors. TSTAR
        # (Myr per N-body time) and VSTAR (km/s per N-body velocity)
        # are populated by Block/units.f during the first CALL NBODY6
        # (lazy-invoked by evolve_model). They return zero quantities
        # until then.
        handler.add_property("get_tstar", public_name="tstar")
        handler.add_property("get_vstar", public_name="vstar")

